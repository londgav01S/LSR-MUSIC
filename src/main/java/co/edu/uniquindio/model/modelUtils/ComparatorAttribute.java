package co.edu.uniquindio.model.modelUtils;

/**
 * Enum representing the different attributes that can be used for comparison.
 */
public enum ComparatorAttribute {
    /**
     * Compare by the name of the song.
     */
    NAME,

    /**
     * Compare by the name of the album.
     */
    ALBUM_NAME,

    /**
     * Compare by the cover of the song or album.
     */
    COVER,

    /**
     * Compare by the year of release.
     */
    YEAR,

    /**
     * Compare by the duration of the song.
     */
    DURATION,

    /**
     * Compare by the genre of the song.
     */
    GENRE,

    /**
     * Compare by the name of the artist.
     */
    ARTIST_NAME
}
